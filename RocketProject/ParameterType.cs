namespace RocketProject
{
    /// <summary>
    /// Тип параметра.
    /// </summary>
    public enum ParameterType
    {
        /// <summary>
        /// Длина ракеты.
        /// </summary>
        RocketLength,

        /// <summary>
        /// Длина конуса.
        /// </summary>
        ConeLength,

        /// <summary>
        /// Длина второй ступени.
        /// </summary>
        SecondStageLength,

        /// <summary>
        /// Длина первой ступени.
        /// </summary>
        FirstStageLength,

        /// <summary>
        /// Диаметр второй ступени.
        /// </summary>
        SecondStageDiameter,

        /// <summary>
        /// Диаметр первой ступени.
        /// </summary>
        FirstStageDiameter,

        /// <summary>
        /// Размах стабилизаторов второй ступени.
        /// </summary>
        SecondStabilizerSpan,
        
        /// <summary>
        /// Размах стабилизаторов первой ступени.
        /// </summary>
        FirstStabilizerSpan,

        /// <summary>
        /// Количество стабилизаторов второй ступени.
        /// </summary>
        SecondStabilizerCount,

        /// <summary>
        /// Количество стабилизаторов первой ступени.
        /// </summary>
        FirstStabilizerCount,

        /// <summary>
        /// Толщина стабилизаторов ступени.
        /// </summary>
        StabilizerWidth,

        /// <summary>
        /// Толщина сопла ракеты.
        /// </summary>
        NozzleDiameter,
       
    }
}
